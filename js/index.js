function according(x) {
    console.log(accord)
    x.classList.toggle("click");
    var first = document.getElementById("accord").classList;
    var isclick = first.contains("click");
    if (isclick) {
        document.getElementById("paragraph's").style.display = "block";
    } else {
        document.getElementById("paragraph's").style.display = "none";
    }
}
